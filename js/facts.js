let factsCache = {};

export async function loadFacts(lang) {
  try {
    const resp = await fetch(`./locales/facts_${lang}.json`);
    if (!resp.ok) throw new Error();
    factsCache = await resp.json();
  } catch {
    const resp = await fetch('./locales/facts_en.json');
    factsCache = await resp.json();
  }
}

export function getCountryFacts(code) {
  const meta = countryMeta[code];
  if (!meta) return null;
  return { ...meta, fact: factsCache[code] || '' };
}
